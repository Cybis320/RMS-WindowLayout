#!/bin/bash
# Generate devilspie2 layout from autostart .desktop files and screen size.
# Usage: ./generate_layout.sh [chars_wide] [font_pixel_width]
#   chars_wide       - terminal column count (default: 229)
#   font_pixel_width - pixel width per character (default: 6)

CHARS_WIDE=${1:-229}
FONT_PX=${2:-6}
AUTOSTART_DIR="$HOME/.config/autostart"
LUA_FILE="$HOME/.config/devilspie2/rms_stations.lua"

# Get screen dimensions
SCREEN_W=$(xdpyinfo 2>/dev/null | awk '/dimensions:/{split($2,a,"x"); print a[1]}')
SCREEN_H=$(xdpyinfo 2>/dev/null | awk '/dimensions:/{split($2,a,"x"); print a[2]}')

if [ -z "$SCREEN_W" ] || [ -z "$SCREEN_H" ]; then
    echo "Error: could not detect screen size" >&2
    exit 1
fi

TOP_BAR=37
SCROLLBAR_PAD=30
ROW_GAP=2

WIN_W=$(( CHARS_WIDE * FONT_PX + SCROLLBAR_PAD ))

# Collect station titles (StartCap profile) and utility titles separately
STATIONS=()
UTILITIES=()

for f in "$AUTOSTART_DIR"/*.desktop; do
    [ -f "$f" ] || continue
    TITLE=$(grep -oP '(?<=--title[= ])\S+' "$f" | head -1)
    [ -z "$TITLE" ] && continue

    if grep -q 'StartCapture' "$f"; then
        STATIONS+=("$TITLE")
    else
        UTILITIES+=("$TITLE")
    fi
done

# Sort stations alphabetically
IFS=$'\n' STATIONS=($(sort <<<"${STATIONS[*]}")); unset IFS

NUM_STATIONS=${#STATIONS[@]}
if [ "$NUM_STATIONS" -eq 0 ]; then
    echo "Error: no station .desktop files found in $AUTOSTART_DIR" >&2
    exit 1
fi

AVAIL_H=$(( SCREEN_H - TOP_BAR ))
ROW_H=$(( (AVAIL_H - (NUM_STATIONS - 1) * ROW_GAP) / NUM_STATIONS ))

# Utility windows: right of stations, stacked from top
UTIL_X=$(( WIN_W + 110 ))
UTIL_W=$(( SCREEN_W - UTIL_X ))
[ "$UTIL_W" -lt 400 ] && UTIL_W=400
UTIL_H=300

# Generate Lua
cat > "$LUA_FILE" << 'HEADER'
-- Auto-generated by generate_layout.sh
-- Re-run the script after changing stations or monitors

local positions = {
HEADER

Y=$TOP_BAR
for S in "${STATIONS[@]}"; do
    printf '    %-18s= { %4d, %4d, %4d, %3d},\n' "[\"$S\"]" 0 "$Y" "$WIN_W" "$ROW_H" >> "$LUA_FILE"
    Y=$(( Y + ROW_H + ROW_GAP ))
done

# Utilities
if [ ${#UTILITIES[@]} -gt 0 ]; then
    echo "    -- Utilities" >> "$LUA_FILE"
    UY=$TOP_BAR
    for U in "${UTILITIES[@]}"; do
        printf '    %-18s= { %4d, %4d, %4d, %3d},\n' "[\"$U\"]" "$UTIL_X" "$UY" "$UTIL_W" "$UTIL_H" >> "$LUA_FILE"
        UY=$(( UY + UTIL_H + ROW_GAP ))
    done
fi

# Known non-desktop windows (matplotlib plots, etc.)
PLOT_H=$(( SCREEN_H - UY ))
[ "$PLOT_H" -lt 300 ] && PLOT_H=300
echo "    -- Plots" >> "$LUA_FILE"
printf '    %-18s= { %4d, %4d, %4d, %3d},\n' '["Figure 1"]' "$UTIL_X" "$UY" "$UTIL_W" "$PLOT_H" >> "$LUA_FILE"

cat >> "$LUA_FILE" << 'FOOTER'
}

local title = get_window_name()
local pos = positions[title]
if pos then
    set_window_position(pos[1], pos[2])
    set_window_size(pos[3], pos[4])
end
FOOTER

echo "Generated $LUA_FILE"
echo "  Screen: ${SCREEN_W}x${SCREEN_H}, stations: $NUM_STATIONS, row height: ${ROW_H}px, width: ${WIN_W}px"
echo "  Stations: ${STATIONS[*]}"
echo "  Utilities: ${UTILITIES[*]}"
echo ""
echo "Restart devilspie2 to apply:  pkill devilspie2; devilspie2 &"
